package statementloops;

public class switchStatement {

	public static void main(String[] args) {

		int years=1;
		
		switch(years)
		{
		case 1:
			System.out.println("Developer Salary for 1st year: 25000");
			break;
		case 2:
			System.out.println("Developer Salary for the 2nd year:35000");
			break;
		
		case 3:
			System.out.println("Developer Salary for the 3nd year:45000");
			break;
			
		}
		
		
	}

}
